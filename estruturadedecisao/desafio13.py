d = input("Numero: ")
d = d.strip()

if d == "1":
    print("1-Domingo")
elif d == "2":
    print("2-Segunda")
elif d == "3":
    print("3-Terça")
elif d == "4":
    print("4-Quarta")
elif d == "5":
    print("5-Quinta")
elif d == "6":
    print("6-Sexta")
elif d == "7":
    print("7-Sabado")
else:
    print("Valor inválido!")